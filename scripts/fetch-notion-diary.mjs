#!/usr/bin/env node
/**
 * Sync the diary Notion database → src/app/data/life-events.ts (read-only).
 *
 * Requires env:
 *   NOTION_TOKEN               — integration secret
 *   NOTION_DIARY_DATABASE_ID   — diary database id (falls back to NOTION_DATABASE_ID)
 *
 * Notion properties expected (same convention as the old Jekyll sync):
 *   Title (title), Date (date), Category (select), Publish (checkbox, optional).
 *   Title (EN), Title (JA) (rich_text, optional) — 英文/日文标题；Title 为中文主标题。
 * The full page body is converted to Markdown and rendered by <MarkdownContent>. To provide
 * the entry in more than one language, tag sections in the page body with their own paragraph
 * reading exactly `---LANG:EN---` / `---LANG:ZH---` / `---LANG:JA---` — see docs/NOTION_SYNC.md.
 * Untagged pages are treated as a single language and shown regardless of site locale (unchanged
 * legacy behavior).
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { Client } from "@notionhq/client";
import {
  readProperty,
  fetchAllPages,
  pageToMarkdown,
  formatDateOnly,
  pageCoverUrl,
  toTs,
  createNotionToMd,
  splitLocalizedMarkdown,
  buildLocalizedTitleFromNotion,
} from "./lib/notion-helpers.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUTPUT_PATH = path.join(ROOT, "src/app/data/life-events.ts");

const NOTION_TOKEN = process.env.NOTION_TOKEN;
const DATABASE_ID = process.env.NOTION_DIARY_DATABASE_ID || process.env.NOTION_DATABASE_ID;

const PROPERTIES = {
  title: "Title",
  titleEn: "Title (EN)",
  titleJa: "Title (JA)",
  date: "Date",
  category: "Category",
  publish: "Publish",
};

if (!NOTION_TOKEN || !DATABASE_ID) {
  console.error(
    "Missing NOTION_TOKEN or NOTION_DIARY_DATABASE_ID.\nSee docs/NOTION_SYNC.md for setup."
  );
  process.exit(1);
}

const notion = new Client({ auth: NOTION_TOKEN });
const n2m = createNotionToMd(notion, ROOT);

function shouldPublish(props) {
  if (!props[PROPERTIES.publish]) return true;
  return readProperty(props, PROPERTIES.publish) === true;
}

async function buildEntry(page) {
  const props = page.properties;
  const notionTitle = readProperty(props, PROPERTIES.title) || "Untitled";
  const titleEn = readProperty(props, PROPERTIES.titleEn);
  const titleJa = readProperty(props, PROPERTIES.titleJa);
  const dateRaw = readProperty(props, PROPERTIES.date) || page.created_time;
  const [year, month, day] = formatDateOnly(dateRaw).split("-").map(Number);
  const category = readProperty(props, PROPERTIES.category) || "Diary";
  const rawBody = await pageToMarkdown(n2m, page.id);
  const imageUrl = await pageCoverUrl(page, ROOT);

  return {
    id: `notion-${page.id}`,
    year,
    month,
    day,
    title: buildLocalizedTitleFromNotion(notionTitle, titleEn, titleJa),
    description: splitLocalizedMarkdown(rawBody),
    category,
    imageUrl,
  };
}

async function main() {
  console.log(`Querying diary database ${DATABASE_ID}...`);
  const pages = await fetchAllPages(notion, DATABASE_ID);
  console.log(`Fetched ${pages.length} page(s) from the database.`);
  const published = pages.filter((page) => shouldPublish(page.properties));
  if (published.length < pages.length) {
    console.log(`${pages.length - published.length} page(s) skipped (Publish unchecked).`);
  }
  const entries = await Promise.all(published.map(buildEntry));

  entries.sort((a, b) => new Date(b.year, b.month - 1, b.day) - new Date(a.year, a.month - 1, a.day));

  const header = `// AUTO-GENERATED by scripts/fetch-notion-diary.mjs — do not edit by hand.
// Source of truth: the diary Notion database. Run \`npm run sync:diary\` to refresh.
import type { LocalizedText } from '../lib/localized';

/** Free-form — comes from whatever value you use in the Notion "Category" select. */
export type LifeCategory = string;

export interface LifeEvent {
  id: string;
  year: number;
  month: number;
  day: number;
  title: LocalizedText;
  /** Markdown source (from Notion, via notion-to-md). Rendered with <MarkdownContent>. */
  description: LocalizedText;
  category: LifeCategory;
  imageUrl?: string;
}
`;

  const body = `export const lifeEvents: LifeEvent[] = ${toTs(entries)};\n`;

  fs.writeFileSync(OUTPUT_PATH, `${header}\n${body}`, "utf8");
  console.log(`Wrote ${entries.length} diary entries to ${path.relative(ROOT, OUTPUT_PATH)}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
